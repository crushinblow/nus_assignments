// Task is to write a JavaScript program that checks if a number is even or odd. 
// Random number 9 is used.
var number = 9;  

// If the number is even, print "Even" to the console. Below divides the assigned number by 2
// Answer is even if its zero.  Note: === is the strict equality operator.
if (number % 2 === 0) {
    console.log("Even");
} else {
    // If the number is odd, print "Odd" to the console. 
    console.log("Odd");
}

